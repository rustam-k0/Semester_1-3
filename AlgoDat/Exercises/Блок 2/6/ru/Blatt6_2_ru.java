import java.util.LinkedList;
import java.util.Queue;
import java.util.Scanner;

public class Blatt6_2_ru {

    public static void main(String[] args) {
        // 1. Подготовка сканера для ввода данных с клавиатуры
        Scanner scanner = new Scanner(System.in);

        System.out.print("Введите число N (какой элемент Фибоначчи вы хотите получить): ");
        int n = scanner.nextInt();

        // Проверка на базовые случаи (первое и второе число всегда 1)
        if (n <= 2) {
            System.out.println("Результат: 1");
            return;
        }

        // 2. Создаем Очередь (Queue).
        // В Java Queue - это интерфейс, а LinkedList - это его реализация.
        // Мы используем тип Long, чтобы поместились большие числа.
        Queue<Long> fibQueue = new LinkedList<>();

        // 3. Заполняем очередь начальными значениями.
        // Последовательность начинается: 1, 1 ...
        // queue.add() добавляет элемент в КОНЕЦ очереди.
        fibQueue.add(1L); // f(1)
        fibQueue.add(1L); // f(2)
        
        // Сейчас в очереди: [1, 1]

        // 4. Основной цикл вычисления.
        // Начинаем с 3-го числа, так как 1-е и 2-е уже известны.
        for (int i = 3; i <= n; i++) {
            // Шаг А: Достаем (удаляем) старое число из НАЧАЛА очереди.
            // poll() возвращает и удаляет первый элемент.
            long first = fibQueue.poll(); 
            // Очередь стала: [1] (осталось только второе число)

            // Шаг Б: Смотрим на текущее число, которое теперь стало первым, но НЕ удаляем его.
            // peek() просто показывает первый элемент.
            long second = fibQueue.peek();
            
            // Шаг В: Считаем новое число Фибоначчи.
            long newNumber = first + second;

            // Шаг Г: Добавляем новое число в КОНЕЦ очереди.
            fibQueue.add(newNumber);
            
            // Теперь в очереди: [1, 2]. Мы сдвинулись на шаг вперед.
        }

        // 5. После окончания цикла в конце очереди лежит наш ответ.
        // Но нам нужно достать последнее добавленное число.
        // В очереди сейчас [f(n-1), f(n)].
        
        fibQueue.poll(); // Удаляем предпоследнее число
        long result = fibQueue.poll(); // Достаем последнее число (наш ответ)

        System.out.println("n-е число Фибоначчи: " + result);
    }
}